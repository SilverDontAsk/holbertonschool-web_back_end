#!/usr/bin/env python3
"""
This function just concatenates two strings together
"""


def concat(str1: str, str2: str) -> str:
    """
    This function takes two strings and adds them together
    Args:
        str1: Expected to be a string
        str2: Expected to be a string
    Return:
        A concatenated string from both str1 and str2
    """
    return str1 + str2
